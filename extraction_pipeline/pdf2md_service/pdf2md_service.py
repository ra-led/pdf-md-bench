#!/usr/bin/env python3
import argparse
import re
import shutil
import subprocess
import sys
from contextlib import contextmanager
from pathlib import Path


DPSK_CONFIG_TEMPLATE = """# Auto-generated by pdf2md service
BASE_SIZE = 1024
IMAGE_SIZE = 640
CROP_MODE = True

MIN_CROPS = 2
MAX_CROPS = 6
MAX_CONCURRENCY = 100
NUM_WORKERS = 64
PRINT_NUM_VIS_TOKENS = False
SKIP_REPEAT = False

MODEL_PATH = '{model_path}'

INPUT_PATH = r"{pdf_path}"
OUTPUT_PATH = r"{result_path}"

PROMPT = '<image>\\n<|grounding|>Convert the document to markdown. '

from transformers import AutoTokenizer
TOKENIZER = AutoTokenizer.from_pretrained(MODEL_PATH, trust_remote_code=True)
"""

PAGE_SPLIT_PATTERN = re.compile(r"(?:\r?\n)*<--- Page Split --->(?:\r?\n)*")
TAG_PATTERN = re.compile(
    r"<\|ref\|>(?P<ref>.+?)<\|/ref\|><\|det\|>\s*\[\[(?P<bbox>[^\]]+)\]\]\s*<\|/det\|>",
    flags=re.DOTALL,
)


@contextmanager
def atomic_replace(file_path: Path, new_content: str):
    original = file_path.read_text(encoding="utf-8") if file_path.exists() else None
    file_path.write_text(new_content, encoding="utf-8")
    try:
        yield
    finally:
        if original is None:
            file_path.unlink(missing_ok=True)
        else:
            file_path.write_text(original, encoding="utf-8")


def run_ocr_with_deepseek_runner(
    *,
    pdf_path: Path,
    dpsk_workdir: Path,
    raw_ocr_root: Path,
    dpsk_model_path: str,
    python_bin: str,
    run_script: str,
    template: str,
) -> Path:
    dpsk_workdir = dpsk_workdir.resolve()
    raw_doc_dir = raw_ocr_root / pdf_path.stem
    if raw_doc_dir.exists():
        shutil.rmtree(raw_doc_dir)
    raw_doc_dir.mkdir(parents=True, exist_ok=True)

    config_path = dpsk_workdir / "config.py"
    config_text = template.format(
        model_path=dpsk_model_path,
        pdf_path=str(pdf_path.resolve()),
        result_path=str(raw_doc_dir.resolve()),
    )

    run_cmd = [python_bin, run_script]
    with atomic_replace(config_path, config_text):
        process = subprocess.Popen(
            run_cmd,
            cwd=dpsk_workdir,
            stdout=subprocess.PIPE,
            stderr=subprocess.STDOUT,
            text=True,
            bufsize=1,
        )

        assert process.stdout is not None
        for line in process.stdout:
            print(line, end="")

        ret = process.wait()
        if ret != 0:
            raise subprocess.CalledProcessError(ret, run_cmd)

    det_candidates = sorted(
        raw_doc_dir.rglob("*_det.mmd"),
        key=lambda p: p.stat().st_mtime,
        reverse=True,
    )
    if det_candidates:
        return det_candidates[0]

    mmd_candidates = sorted(
        raw_doc_dir.rglob("*.mmd"),
        key=lambda p: p.stat().st_mtime,
        reverse=True,
    )
    if not mmd_candidates:
        raise FileNotFoundError(f"No .mmd output found in {raw_doc_dir}")
    return mmd_candidates[0]


def _normalize_bbox(raw_bbox: str) -> str:
    parts = [value.strip() for value in raw_bbox.replace("[", "").replace("]", "").split(",")]
    return " ".join(filter(None, parts))


def _strip_tags(content: str) -> str:
    def _replacer(match: re.Match[str]) -> str:
        ref_type = match.group("ref").strip().lower()
        bbox = _normalize_bbox(match.group("bbox"))
        if ref_type == "image" and bbox:
            return f'<img data-bbox="{bbox}"></img>\n'
        return ""

    cleaned = TAG_PATTERN.sub(_replacer, content)
    cleaned = "\n".join(line.rstrip() for line in cleaned.splitlines())
    cleaned = re.sub(r"\n{3,}", "\n\n", cleaned)
    return cleaned.strip()


def _split_pages(raw_text: str) -> list[str]:
    if not raw_text.strip():
        return []
    segments = PAGE_SPLIT_PATTERN.split(raw_text.strip())
    return segments if segments else []


def convert_document(det_file: Path, output_dir: Path) -> None:
    raw_text = det_file.read_text(encoding="utf-8").replace("\r\n", "\n")
    pages_raw = _split_pages(raw_text)
    if not pages_raw:
        pages_raw = [raw_text]

    doc_out_dir = output_dir / det_file.parent.name
    if doc_out_dir.exists():
        shutil.rmtree(doc_out_dir)
    doc_out_dir.mkdir(parents=True, exist_ok=True)

    page_outputs: list[str] = []
    for index, page in enumerate(pages_raw):
        converted = _strip_tags(page)
        page_outputs.append(converted)
        page_path = doc_out_dir / f"page_{index:03d}.md"
        page_path.write_text((converted + "\n") if converted else "\n", encoding="utf-8")

    combined_path = doc_out_dir / f"{det_file.parent.name}.md"
    combined_content = "\n\n".join(text for text in page_outputs if text)
    combined_path.write_text((combined_content + "\n") if combined_content else "\n", encoding="utf-8")


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description="Convert PDF directory to Markdown with DeepSeek OCR.")
    parser.add_argument("--input_dir", required=True, help="Directory with input PDF files.")
    parser.add_argument("--output_dir", required=True, help="Directory for output Markdown files.")
    parser.add_argument(
        "--raw_ocr_dir",
        default="/workspace/raw_ocr",
        help="Intermediate raw OCR directory inside container.",
    )
    parser.add_argument(
        "--model_path",
        default="deepseek-ai/DeepSeek-OCR",
        help="Model path for DeepSeek OCR.",
    )
    parser.add_argument(
        "--dpsk_workdir",
        default="/app/vendor/DeepSeek-OCR-vllm",
        help="Path to DeepSeek-OCR-vllm source.",
    )
    return parser.parse_args()


def main() -> None:
    args = parse_args()
    input_dir = Path(args.input_dir)
    output_dir = Path(args.output_dir)
    raw_ocr_root = Path(args.raw_ocr_dir)
    dpsk_workdir = Path(args.dpsk_workdir)

    if not input_dir.exists():
        raise FileNotFoundError(f"Input dir does not exist: {input_dir}")

    output_dir.mkdir(parents=True, exist_ok=True)
    raw_ocr_root.mkdir(parents=True, exist_ok=True)

    pdf_files = sorted(input_dir.glob("*.pdf"))
    if not pdf_files:
        print(f"No PDF files found in {input_dir}")
        return

    for pdf_path in pdf_files:
        print(f"[pdf2md] Processing: {pdf_path.name}")
        det_file = run_ocr_with_deepseek_runner(
            pdf_path=pdf_path,
            dpsk_workdir=dpsk_workdir,
            raw_ocr_root=raw_ocr_root,
            dpsk_model_path=args.model_path,
            python_bin=sys.executable,
            run_script="run_dpsk_ocr_pdf_with_retries.py",
            template=DPSK_CONFIG_TEMPLATE,
        )
        convert_document(det_file, output_dir)
        print(f"[pdf2md] Done: {pdf_path.name}")

    print(f"[pdf2md] Completed. Markdown output: {output_dir}")


if __name__ == "__main__":
    main()
