#!/usr/bin/env python3
"""Shared DeepSeek OCR runner utilities."""

from __future__ import annotations

import subprocess
from contextlib import contextmanager
from pathlib import Path

DPSK_CONFIG_TEMPLATE = """# Auto-generated by pipeline script
BASE_SIZE = 1024
IMAGE_SIZE = 640
CROP_MODE = True

MIN_CROPS = 2
MAX_CROPS = 6
MAX_CONCURRENCY = 100
NUM_WORKERS = 64
PRINT_NUM_VIS_TOKENS = False
SKIP_REPEAT = True
MODEL_PATH = '{model_path}'

INPUT_PATH = r"{pdf_path}"
OUTPUT_PATH = r"{result_path}"

PROMPT = '<image>\\n<|grounding|>Convert the document to markdown.'

from transformers import AutoTokenizer
TOKENIZER = AutoTokenizer.from_pretrained(MODEL_PATH, trust_remote_code=True)
"""


@contextmanager
def atomic_replace(file_path: Path, new_content: str):
    original = file_path.read_text(encoding="utf-8") if file_path.exists() else None
    file_path.write_text(new_content, encoding="utf-8")
    try:
        yield
    finally:
        if original is None:
            file_path.unlink(missing_ok=True)
        else:
            file_path.write_text(original, encoding="utf-8")


def run_ocr_with_deepseek_runner(
    *,
    pdf_path: Path,
    dpsk_workdir: Path,
    raw_ocr_root: Path,
    dpsk_model_path: str,
    python_bin: str,
    run_script: str,
) -> Path:
    dpsk_workdir = dpsk_workdir.resolve()
    print(f"[STEP] Running OCR with DeepSeek runner from: {dpsk_workdir}")

    raw_doc_dir = raw_ocr_root / pdf_path.stem
    raw_doc_dir.mkdir(parents=True, exist_ok=True)

    config_path = dpsk_workdir / "config.py"
    config_text = DPSK_CONFIG_TEMPLATE.format(
        model_path=dpsk_model_path,
        pdf_path=str(pdf_path.resolve()),
        result_path=str(raw_doc_dir.resolve()),
    )

    run_cmd = [python_bin, run_script]
    print(f"[INFO] OCR command: {' '.join(run_cmd)}")
    with atomic_replace(config_path, config_text):
        subprocess.run(run_cmd, cwd=dpsk_workdir, check=True)

    det_candidates = sorted(raw_doc_dir.rglob("*_det.mmd"))
    if not det_candidates:
        mmd_candidates = sorted(raw_doc_dir.rglob("*.mmd"))
        if not mmd_candidates:
            raise FileNotFoundError(f"No .mmd output found in {raw_doc_dir}")
        return mmd_candidates[0]
    return det_candidates[0]
